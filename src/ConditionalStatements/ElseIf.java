package ConditionalStatements;

public class ElseIf {
    public static void main(String[] args) {
        int b=13;
        if (b==18) {
            System.out.println("true1");
        } else if (b>=12) {
            System.out.println("true2");
        } else if (b<=16) {
            System.out.println("true3");

        }
    }
}
